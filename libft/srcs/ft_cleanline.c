#include "ft.h"

char	*ft_cleanline(char const *s1)
{
	char			*str;
	unsigned int	i;

	i = 0;
	str = (char*)s1;
	while (str[i] != '\0')
	{
		if (str[i] == '\n')
			str[i] = '\0';
		i++;
	}
	return (str);
}
